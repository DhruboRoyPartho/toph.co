#include<bits/stdc++.h>

using namespace std;

int main()
{
    int a;
    char b;
    float c;
    double d;
    bool e;
    long int f;
    short int g;
    long long int h;
    unsigned int i;

    cout<<"Size of int "<<sizeof(a)<<endl;
    cout<<"Size of char "<<sizeof(b)<<endl;
    cout<<"Size of float "<<sizeof(c)<<endl;
    cout<<"Size of double "<<sizeof(d)<<endl;
    cout<<"Size of bool "<<sizeof(e)<<endl;
    cout<<"Size of long int "<<sizeof(f)<<endl;
    cout<<"Size of short int "<<sizeof(g)<<endl;
    cout<<"Size of long long int "<<sizeof(h)<<endl;
    cout<<"Size of unsigned int "<<sizeof(i)<<endl;

    return 0;
}
